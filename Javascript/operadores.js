/* 
------------------------------------------------------
| Operador |        Sintaxe       |     Descrição    |
------------------------------------------------------
|          |                      | Inverte o valor  |
|    !     |       !expressão     | booleano da      |
|          |                      | expressão        |
------------------------------------------------------
|          |                      | Converte o valor |
|    !!    |      !!expressão     | da expressão em  |
|          |                      | um booleano      |
------------------------------------------------------
|          |                      |                  |
|          |                      | condição e       |
|          |   condição ? expr1 : | retorna a expr1  |
|    ?     |   expr2              | se verdadeiro,   |
|          |                      | caso contrário,  |
|          |                      | retorna a expr2  |
------------------------------------------------------
|   ??     |                      | Retorna valor1   |
|          |                      | se não for null  |
|          |   valor1 ?? valor2   | nem undefined,   |
|          |                      | caso contrário,  |
|          |                      | retorna valor2   |
------------------------------------------------------
*/